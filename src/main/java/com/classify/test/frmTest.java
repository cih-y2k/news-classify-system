    void getListNews() {
        String line;
        for (int i = 0; i < IConfig.LABEL_COUNT; i++) {
            int count = 0;
            BufferedReader reader = null;
            try {
                reader = new BufferedReader(new FileReader(IConfig.TEST_PATH[i]));
                while (count < MAX_NEWS_TEST && (line = reader.readLine()) != null) {
                    if (line.length() > 300) {
                        lstNews.add(new News(line.trim(), i));
                        count++;
                    }
                }
                total[i] = count;
                reader.close();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(frmTest.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(frmTest.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    reader.close();
                } catch (IOException ex) {
                    Logger.getLogger(frmTest.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        Collections.shuffle(lstNews);
    }

