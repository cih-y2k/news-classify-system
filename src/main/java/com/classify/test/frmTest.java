    private void lstItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstItemMouseClicked
        // TODO add your handling code here:
        JList list = (JList) evt.getSource();
        if (evt.getClickCount() == 2) {
            int index = list.locationToIndex(evt.getPoint());
            txtView.setText(lstNews.get(index).getData());
            lbCorrect.setText("Correct Label: " + IConfig.LABELS[lstNews.get(index).getCorrectLabel()]);
            int label = lstNews.get(index).getDetectLabel();
            if (label == -1) {
                lbDetect.setText("Detect Label: " + "undefined");
            } else {
                lbDetect.setText("Detect Label: " + IConfig.LABELS[label]);
            }
        }
    }//GEN-LAST:event_lstItemMouseClicked

    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        int label;
        for (News i : lstNews) {
            label = detecter.getLabel(i.getData());
            if (label == i.getCorrectLabel()) {
                correct[label]++;
            }
            i.setDetectLabel(label);
        }
        lb1.setText("Education: " + correct[0] + "/" + total[0]);
        lb2.setText("Science: " + correct[1] + "/" + total[1]);
        lb3.setText("Business: " + correct[2] + "/" + total[2]);
        lb4.setText("Law: " + correct[3] + "/" + total[3]);
        lb5.setText("Health: " + correct[4] + "/" + total[4]);
        lb6.setText("Sport: " + correct[5] + "/" + total[5]);
    }//GEN-LAST:event_btnOkActionPerformed

